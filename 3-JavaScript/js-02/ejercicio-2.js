/* ------ Función Expresión o Anónima ------ */
let suma = function (a, b){ return (a + b) };
let res = suma(2, 3)
console.log("El resultado de la suma es: " + res)


let  resta = function (a, b){ return (a - b) };
let res1 = resta(10, 5)
console.log("El resultado de la resta es: " + res1)


let multiplicacion = function (a, b){ return (a * b) };
let res2 = multiplicacion(6, 3)
console.log("El resultado de la multiplicacion es: " + res2)


let division = function (a, b){ return (a / b) };
let res3 = division(4, 2)
console.log("El resultado de la division es: " + res3)