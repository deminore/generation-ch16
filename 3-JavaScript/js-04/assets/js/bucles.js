/* ---------- BUCLES ---------- */
/* ------- for ------- */
for(let i = 0; i < 5; i++)
{
    console.log(i);
}


/* ------- while ------- */
let control = 0;

while (control < 5) {
    console.log("While " + control);

    control++;
}


/* ------- do - while ------- */
let control1 = 0;
do {
    console.log("Do... while " + control1);
    control1 ++;

} while (control1 < 5);
