// Operadores de Comparación

// Igualdad
console.log("Igualdad");
console.log(5 == 5);
console.log(1 == 3);

console.log(5 == "5"); // Caso especial

console.log("hola" == "adios");
console.log("hola" == "hola ");

console.log(true == false);

// Desigualdad
console.log("Desigualdad");
console.log(5 != 1);
console.log("gato" != "gato");

// Mayor
console.log("Mayor");
console.log(10 > 5);
console.log(1 > 5);
console.log(5 > 5);

console.log("Mayor o igual");
console.log(5 >= 5);
console.log(10 >= 3);
console.log(3 >= 7);

// Menor
console.log("Menor");
console.log(10 < 20);
console.log(20 < 10);

console.log("Menor o igual");
console.log(20 <= 20);

console.log("Números");
console.log("D" > "A");
